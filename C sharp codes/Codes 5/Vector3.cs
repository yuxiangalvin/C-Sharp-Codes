namespace Assignment5
{
    /// <summary>
    /// Represents a point in 3-space
    /// </summary>
    public struct Vector3
    {
        public Vector3(float x, float y, float z)
        {
            X = x;
            Y = y;
            Z = z;
        }

        public float X;
        public float Y;
        public float Z;
    }
}
